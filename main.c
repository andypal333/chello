// A simple C program to display the message "Hello World"

// Include the standard input/output header file
// This header file is necessary for using the printf function
#include <stdio.h>

// The main function serves as the entry point of the program
// Program execution starts from the main function
int main()
{
    // Use the printf function to output the message "Hello World" to the console
    // printf is a standard library function defined in <stdio.h>
    printf("Hello World");

    // Return 0 to indicate successful completion of the program
    // The return value of main is used as the exit status of the program
    return 0;
}
